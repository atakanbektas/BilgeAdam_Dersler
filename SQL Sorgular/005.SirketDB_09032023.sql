CREATE TABLE Il(
  ProjeId INT,
  IlId INT IDENTITY(1,1),
  IlAd NVARCHAR(50),
  FOREIGN KEY (ProjeId) REFERENCES Proje(ProjeId),
  PRIMARY KEY (IlId) 
