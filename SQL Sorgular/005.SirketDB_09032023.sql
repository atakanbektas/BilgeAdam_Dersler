  PersonelTC NVARCHAR(11) UNIQUE,
  PersonelAdi NVARCHAR(50),
  PersonelSoyadi NVARCHAR(50),
  Cinsiyet NVARCHAR(25),
  DogumTarihi DATE,
  DogumYeri NVARCHAR(50),
  BaslamaTarihi DATE, 
  BirimNo INT,
  UnvanNo INT,
  Maas MONEY,
  Prim MONEY
  PRIMARY KEY (PersonelTC)
)

